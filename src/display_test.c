static int init_pins(const struct shell *sh)
{
	int ret = gpio_pin_configure_dt(&display_reset, GPIO_OUTPUT_ACTIVE);
	if (ret < 0) {
		shell_fprintf(sh, SHELL_VT100_COLOR_RED, "reset pin configuration failed");
		return ret;
	}

	ret = gpio_pin_set_dt(&display_reset, 1);
	if (ret < 0) {
		shell_fprintf(sh, SHELL_VT100_COLOR_RED, "reset pin set failed");
		return ret;
	}

	ret = gpio_pin_configure_dt(&display_en, GPIO_OUTPUT_ACTIVE);
	if (ret < 0) {
		shell_fprintf(sh, SHELL_VT100_COLOR_RED, "enable pin configuration failed");
		return ret;
	}

	ret = gpio_pin_set_dt(&display_en, 1);
	if (ret < 0) {
		shell_fprintf(sh, SHELL_VT100_COLOR_RED, "enable pin set failed");
		return ret;
	}

	return 0;
}

static void display_clear_and_flush(void)
{
	int ret = gpio_pin_set_dt(&display_en, 0);
	if (ret < 0) {
		return;
	}

	ret = gpio_pin_set_dt(&display_reset, 1);
	if (ret < 0) {
		return;
	}

	ret = gpio_pin_set_dt(&display_reset, 0);
	if (ret < 0) {
		return;
	}
}

int cmd_test_display(const struct shell *sh, size_t argc, char **argv)
{
	int ret;
	display_init();

	ret = init_pins(sh);
	if (ret < 0) {
		shell_fprintf_impl(sh, SHELL_VT100_COLOR_RED, "pin initialization failed");
		return ret;
	}

	shell_fprintf_impl(sh, SHELL_VT100_COLOR_GREEN, "Pins initialized successfully\n");

	display_enable();

	display_clear();

	display_set_draw_mode(DISPLAY_PIXEL_WHITE);

	display_draw_circle(64, 32, 30);

	display_draw_circle(64, 32, 29);

	display_draw_circle(64, 32, 28);

	display_set_draw_mode(DISPLAY_PIXEL_INVERT);

	display_print("Hello, World!", 27, 19, FONT_SIZE_10, 1);

	display_set_draw_mode(DISPLAY_PIXEL_WHITE);

	display_flush();

	k_msleep(5000);

	display_clear();

	display_draw_bitmap(zbook_bitmap, 12, 7, 100, 50, 1);

	display_flush();

	k_msleep(5000);

	display_clear_and_flush();

	return 0;
}
